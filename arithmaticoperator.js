// ++ and -- operator
//for preincrement and post increment
let x1=2
let y1=x1++
console.log(x1)
console.log(y1)

let y2=++x1
console.log(x1)
console.log(y2)

//for predecrement and post decrement
let s1=2
let w1=s1--
console.log(s1)
console.log(w1)

let a2=--s1
console.log(s1)
console.log(a2)
